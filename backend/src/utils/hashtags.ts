/**
 * Хэштеги Content Factory — единый источник правды = массив (PostVersion.hashtags / proposedTags).
 *
 * Проблема (баг «дубль хэштегов»): AI-копирайтер/адаптер иногда вписывает хэштеги
 * ИНЛАЙН в текст поста («…вода\n\n#НаWоде #SUP»), а паблишер ДОПОЛНИТЕЛЬНО клеит
 * массив version.hashtags в конец → два набора тегов (в тексте одни, в массиве другие).
 *
 * Решение: вырезать инлайн-теги из текста, оставив массив единственным источником.
 * Pure-функция (без side-effects, без AI) — по образцу utils/utm.ts.
 */

// Токен хэштега: только если '#' стоит в начале строки или после пробела (через захват границы $1).
// Так мы НЕ трогаем '#' внутри URL-фрагмента (https://nawode.ru/p#section) — там перед '#' буква.
// Класс [^\s#]+ ловит смешанные теги вроде #НаWоде (кириллица + латиница).
const INLINE_HASHTAG_RE = /(^|\s)#[^\s#]+/gu

/**
 * Вырезать инлайн-хэштеги из текста + причесать пробелы/пустые строки.
 *  "…вода\n\n#НаWоде #SUP #Выборг" → "…вода"
 *  "приходи #сап кататься"          → "приходи кататься"
 *  "ссылка https://nawode.ru/p#sec" → без изменений (это не хэштег, а фрагмент URL)
 */
export function stripInlineHashtags(text: string): string {
  if (!text) return text
  // Цикл — чтобы добить подряд идущие теги (после удаления одного открывается граница для следующего).
  let out = text
  let prev: string
  do {
    prev = out
    out = out.replace(INLINE_HASHTAG_RE, '$1')
  } while (out !== prev)
  return out
    .replace(/[ \t]{2,}/g, ' ')   // схлопнуть пробелы/табы, оставшиеся от удалённых тегов
    .replace(/[ \t]+$/gm, '')      // убрать хвостовые пробелы в каждой строке
    .replace(/\n{3,}/g, '\n\n')    // не больше одной пустой строки подряд
    .trim()
}

/** Есть ли в тексте инлайн-хэштеги (для диагностики/тестов). */
export function hasInlineHashtags(text: string): boolean {
  if (!text) return false
  return /(^|\s)#[^\s#]+/u.test(text)
}
