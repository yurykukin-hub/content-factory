/**
 * Чистка заголовка сторис/поста для дизайн-картинки и БД (Фаза 3, фидбек Юрия).
 *
 * Применяется ПРИ СОХРАНЕНИИ предложения — детерминированно покрывает ВСЕ генераторы
 * (роль/лента-стратег/flash), а не каждый промпт по отдельности (агент упрям, temp 0.9):
 * - убирает конкретную температуру «+20°C» (она уже в погодном виджете → дубль с другим числом);
 * - меняет сухое «температура» → «погода» (звучит живее — решение Юрия);
 * - убирает ведущий день недели «Вторник — …» (день на картинке не нужен, был в плохом паттерне);
 * - схлопывает пробелы, чистит висячие тире, капитализирует.
 */
export function cleanStoryTitle(s: string): string {
  return (s || '')
    .replace(/[+\-−]?\s?\d{1,2}\s?°\s?[CСcс]?/g, '')                                                          // +20°C · 20° · −5 °C
    .replace(/температур[а-яё]*/gi, 'погода')                                                                 // «идеальная температура» → «идеальная погода» (\w НЕ ловит кириллицу!)
    .replace(/^\s*(понедельник[а-яё]*|вторник[а-яё]*|сред[а-яё]*|четверг[а-яё]*|пятниц[а-яё]*|суббот[а-яё]*|воскресень[а-яё]*|воскресн[а-яё]*)\s*[—–,-]*\s*/i, '') // ведущий день недели
    .replace(/\s{2,}/g, ' ')
    .replace(/^[\s—–,-]+|[\s—–,-]+$/g, '')
    .trim()
    .replace(/^./, c => c.toUpperCase())
}
